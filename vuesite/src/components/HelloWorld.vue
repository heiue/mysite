<template>
  <div class="hello">
    <h1>{{ msg }}</h1>
  </div>
</template>

<script>
import axios from 'axios'
export default {
  name: 'HelloWorld',
  data () {
    return {
      msg: 'Welcome to Your Vue.js App'
    }
  },
  mounted () {
    this.getData()
  },
  methods: {
    getData: function () {
      axios.get('http://127.0.0.1:8000/api/hello').then(response => {
        console.log(response)
        this.msg = response.data.data.msg
      })
    }
  }
}
</script>

